def sum_values(num_one, num_two):
    print(f"{num_one} + {num_two} = {num_one + num_two}")


def subtrack_values(num_one, num_two):
    print(f"{num_one} - {num_two} = {num_one - num_two}")


def multiply_values(num_one, num_two):
    print(f"{num_one} * {num_two} = {num_one * num_two}")


def divide_values(num_one, num_two):
    print(f"{num_one} / {num_two} = {num_one / num_two}")